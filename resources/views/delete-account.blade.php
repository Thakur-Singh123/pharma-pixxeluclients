            <div class="policy-section">
                <h2>1. Account Deletion</h2>
                <p>
                    Users may request deletion of their Ad People account at any time through the authorized
                    system administrator or designated support channel.
                </p>
                <p>
                    As Ad People operates as a role-based hospital management system, account deletion requests
                    are processed after standard verification and administrative approval. Once approved, the
                    account is deactivated and permanently deleted in accordance with internal policies.
                </p>
            </div>
            <div class="policy-section">
                <h2>2. Data Handling After Deletion</h2>
                <p>After account deletion:</p>
                <ul>
                    <li>User access to the system is permanently disabled.</li>
                    <li>Personal profile information and login credentials are removed from active systems.</li>
                    <li>
                        Certain operational records, such as reports, attendance logs, or audit trails,
                        may be securely retained in a restricted or anonymized format to meet legal,
                        compliance, or administrative requirements. These records are no longer linked
                        to an active user account.
                    </li>
                </ul>
            </div>
            <div class="policy-section">
                <h2>3. Data Security & Breach Response</h2>
                <p>
                    Ad People follows standard security practices, including role-based access control
                    and secure authentication.
                </p>
                <p>
                    In the event of unauthorized access or a data breach, appropriate actions will be taken
                    to investigate the incident, secure affected systems, and implement measures to prevent
                    recurrence. Notifications will be provided where required by applicable regulations.
                </p>
            </div>
            <div class="policy-section">
                <h2>4. User Responsibilities</h2>
